package com.callteam.dto;

public class DashboardDto {

    public Integer totalPlayers;
    public Integer totalStadiums;
    public Integer totalSports;
    public Integer totalMatches;
    public Integer totalTournaments;
    public Integer totalInProgressMatches;
    public Integer totalInProgressTournaments;

    public Integer getTotalPlayers() {
        return totalPlayers;
    }

    public void setTotalPlayers(Integer totalPlayers) {
        this.totalPlayers = totalPlayers;
    }

    public Integer getTotalStadiums() {
        return totalStadiums;
    }

    public void setTotalStadiums(Integer totalStadiums) {
        this.totalStadiums = totalStadiums;
    }

    public Integer getTotalSports() {
        return totalSports;
    }

    public void setTotalSports(Integer totalSports) {
        this.totalSports = totalSports;
    }

    public Integer getTotalMatches() {
        return totalMatches;
    }

    public void setTotalMatches(Integer totalMatches) {
        this.totalMatches = totalMatches;
    }

    public Integer getTotalTournaments() {
        return totalTournaments;
    }

    public void setTotalTournaments(Integer totalTournaments) {
        this.totalTournaments = totalTournaments;
    }

    public Integer getTotalInProgressMatches() {
        return totalInProgressMatches;
    }

    public void setTotalInProgressMatches(Integer totalInProgressMatches) {
        this.totalInProgressMatches = totalInProgressMatches;
    }

    public Integer getTotalInProgressTournaments() {
        return totalInProgressTournaments;
    }

    public void setTotalInProgressTournaments(Integer totalInProgressTournaments) {
        this.totalInProgressTournaments = totalInProgressTournaments;
    }
}
